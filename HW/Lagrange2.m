%% Lagrange Interpolation
clear all; close all; clc;

%% load data
X = [0 15 30 45 60 75 90];
Y = [4  5 4.5 6 6  5 2.5];

%% Define x-domain,
x = 0:1:90;

%% Define function A(x)
A = @(x) 2.*( ...
      Y(1).*(x-X(2)).*(x-X(3)).*(x-X(4)).*(x-X(5)).*(x-X(6)).*(x-X(7))/(X(1)-X(2)).*(X(1)-X(3)).*(X(1)-X(4)).*(X(1)-X(5)).*(X(1)-X(6)).*(X(1)-X(7))  ...
    + Y(2).*(x-X(1)).*(x-X(3)).*(x-X(4)).*(x-X(5)).*(x-X(6)).*(x-X(7))/(X(2)-X(1)).*(X(2)-X(3)).*(X(2)-X(4)).*(X(2)-X(5)).*(X(2)-X(6)).*(X(2)-X(7))  ...
    + Y(3).*(x-X(1)).*(x-X(2)).*(x-X(4)).*(x-X(5)).*(x-X(6)).*(x-X(7))/(X(3)-X(1)).*(X(3)-X(2)).*(X(3)-X(4)).*(X(3)-X(5)).*(X(3)-X(6)).*(X(3)-X(7))  ...
	+ Y(4).*(x-X(1)).*(x-X(2)).*(x-X(3)).*(x-X(5)).*(x-X(6)).*(x-X(7))/(X(4)-X(1)).*(X(4)-X(2)).*(X(4)-X(3)).*(X(4)-X(5)).*(X(4)-X(6)).*(X(4)-X(7))  ...
	+ Y(5).*(x-X(1)).*(x-X(2)).*(x-X(3)).*(x-X(4)).*(x-X(6)).*(x-X(7))/(X(5)-X(1)).*(X(5)-X(2)).*(X(5)-X(3)).*(X(5)-X(4)).*(X(5)-X(6)).*(X(5)-X(7))  ...
	+ Y(6).*(x-X(1)).*(x-X(2)).*(x-X(3)).*(x-X(4)).*(x-X(5)).*(x-X(7))/(X(6)-X(1)).*(X(6)-X(2)).*(X(6)-X(3)).*(X(6)-X(4)).*(X(6)-X(5)).*(X(6)-X(7))  ...
	+ Y(7).*(x-X(1)).*(x-X(2)).*(x-X(3)).*(x-X(4)).*(x-X(5)).*(x-X(6))/(X(7)-X(1)).*(X(7)-X(2)).*(X(7)-X(3)).*(X(7)-X(4)).*(X(7)-X(5)).*(X(7)-X(6))  ...
    );

% Plot Polynomial
plot(x,A(x),'or')